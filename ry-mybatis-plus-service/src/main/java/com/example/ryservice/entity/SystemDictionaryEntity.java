package com.example.ryservice.entity;


public class SystemDictionaryEntity {

    private Long id;

    private String name;

    private String number;

    private String val;

    /**
     * 获取id。
     *
     * @return id
     */
    public Long getId() {
        return id;
    }

    /**
     * 设置id。
     *
     * @param id id
     */
    public void setId(Long id) {
        this.id = id;
    }

    /**
     * 获取name。
     *
     * @return name
     */
    public String getName() {
        return name;
    }

    /**
     * 设置name。
     *
     * @param name name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * 获取number。
     *
     * @return number
     */
    public String getNumber() {
        return number;
    }

    /**
     * 设置number。
     *
     * @param number number
     */
    public void setNumber(String number) {
        this.number = number;
    }

    /**
     * 获取val。
     *
     * @return val
     */
    public String getVal() {
        return val;
    }

    /**
     * 设置val。
     *
     * @param val val
     */
    public void setVal(String val) {
        this.val = val;
    }
}
